int rsa_keygen_sw(struct rsa_keygen *rsa_keygen)
{
	int num, ret = 1;
	unsigned long f4 = RSA_F4;
	struct keygen_buf *buf, buffer;

	BIGNUM *bn = BN_new();
	RSA *rsa = NULL;

	print_debug("DEBUG: %s\n", __func__);
	buf = &buffer;
	num = rsa_keygen->n_len * 8;

	if (!bn)
		goto err;
	rsa = RSA_new();
	if (!rsa)
		goto err;

	if (!BN_set_word(bn, f4) || !RSA_generate_key_ex(rsa, num, bn, NULL))
		goto err;

	get_keygen_buf(buf, rsa_keygen->n_len * 8);

	BN_bn2bin(rsa->p, buf->p);
	BN_bn2bin(rsa->q, buf->q);
	BN_bn2bin(rsa->dmp1, buf->dp);
	BN_bn2bin(rsa->dmq1, buf->dq);
	BN_bn2bin(rsa->iqmp, buf->c);
	ret_pub_key(rsa_keygen, rsa);

	ret = encrypt_priv_key_to_blob(blob_sec, RSA_KEY_FILE, key_buf_start,
				get_rsa_keys_size());

	if (ret != 1)
		goto err;

	RSA_free(rsa);
	BN_free(bn);

	return 1;
err:
	return 0;
}

void assign_rsa_key(void *buf)
{
	phys_addr_t tmp;
	int i;
	static int first = 0;

	if (buf == NULL) {
		buf = get_buffer(get_rsa_keys_size());
		if (buf == NULL) {
			printf("error: no Memory\n");
			exit(0);
		}
		first = 1;
	}

	key_buf_start = buf;
	tmp  = va_to_pa((va_addr_t)buf);

	for (i = 0; i < ARRAY_SIZE(key_info); i++) {
		key_info[i].p_data = tmp;
		if (first) {
			memcpy((u8 *)pa_to_va(tmp), key_info[i].data,
					key_info[i].len);
			print_debug("set default key\n");
		}
		tmp += key_info[i].len;
	}
}
